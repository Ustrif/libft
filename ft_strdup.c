#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*result;
	int		i;

	result = malloc((ft_strlen(src) + 1) * sizeof(char));
	i = 0;
	if (result == NULL)
		return (NULL);
	while (src[i])
	{
		result[i] = src[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
